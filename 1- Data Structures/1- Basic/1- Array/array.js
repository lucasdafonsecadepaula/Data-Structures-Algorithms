// Inicialização de um array com alguns números
const numbers = [10, 20, 30, 40, 50];

// Acessando elementos do array
console.log("Primeiro número:", numbers[0]);  // Output: 10
console.log("Segundo número:", numbers[1]);   // Output: 20

// Modificando um elemento do array
numbers[2] = 35;
console.log("Array atualizado:", numbers);    // Output: [10, 20, 35, 40, 50]